def extract_import_targets(root: Path, rel_path: str, limit: int = 5) -> list[str]:
    path = root / rel_path
    if not path.exists() or not path.is_file():
        return []
    text = read_text(path)
    imports: list[str] = []
    for match in IMPORT_RE.finditer(text):
        target = match.group(1)
        if not target:
            continue
        imports.append(target)
        if len(imports) >= limit:
            break
    return imports


def collect_architecture_chain(root: Path) -> list[dict[str, Any]]:
    chain_candidates = [
        "app/layout.tsx",
        "app/page.tsx",
        "app/[[...slug]]/page.tsx",
        "src/entry/AdminClientApp.tsx",
        "src/main.tsx",
        "src/main.ts",
        "src/App.tsx",
        "src/router/index.tsx",
        "src/router/index.ts",
        "src/router/default.ts",
        "src/api/index.ts",
        "src/api/service.ts",
        "src/stores",
        "src/domain",
    ]
    rows: list[dict[str, Any]] = []
    for rel in find_existing_paths(root, chain_candidates):
        role = next((item["role"] for item in collect_entry_points(root) if item["path"] == rel), "")
        rows.append(
            {
                "path": rel,
                "role": role or infer_chain_role(rel),
                "imports": extract_import_targets(root, rel),
            }
        )
    return rows


def infer_chain_role(rel: str) -> str:
    lower = rel.lower()
    if "layout" in lower:
        return "布局或框架外壳"
    if "/page" in lower or lower.endswith("page.tsx"):
        return "页面入口"
    if "/router" in lower:
        return "路由组合层"
    if "/api/" in lower:
        return "数据访问层"
    if "/stores" in lower:
        return "状态层"
    if "/domain" in lower:
        return "领域类型层"
    if lower.endswith("app.tsx"):
        return "应用组合层"
    if lower.endswith("main.tsx") or lower.endswith("main.ts"):
        return "启动入口"
    return "架构信号文件"


def collect_data_layer_signals(root: Path) -> list[dict[str, Any]]:
    candidates = [
        "src/api",
        "src/services",
        "src/stores",
        "src/store",
        "src/domain",
        "src/models",
        "src/server",
        "prisma",
        "db",
        "database",
        "migrations",
        "supabase",
    ]
    rows: list[dict[str, Any]] = []
    for rel in candidates:
        path = root / rel
        if not path.exists():
            continue
        if path.is_dir():
            rows.append(
                {
                    "path": rel,
                    "kind": "directory",
                    "file_count": count_source_files(path),
                }
            )
        else:
            rows.append({"path": rel, "kind": "file", "file_count": 1})
    return rows


def collect_route_signals(root: Path) -> list[str]:
    signals: list[str] = []
    if (root / "app").exists():
        app_pages = len(list((root / "app").glob("**/page.*")))
        app_routes = len(list((root / "app").glob("**/route.*")))
        signals.append(f"`app/` 下检测到约 {app_pages} 个页面文件、{app_routes} 个 route handler")
    if (root / "pages").exists():
        pages_count = len(list((root / "pages").glob("**/*.*")))
        signals.append(f"`pages/` 目录存在，约 {pages_count} 个文件")
    for rel in ["src/router", "src/routes", "router", "routes"]:
        path = root / rel
        if path.exists():
            signals.append(f"检测到路由目录 {rel}")
    return signals


def collect_module_areas(root: Path, limit: int = 12) -> list[dict[str, Any]]:
    areas: list[dict[str, Any]] = []
    for rel in MODULE_HINT_DIRS:
        base = root / rel
        if not base.exists() or not base.is_dir():
            continue
        children = [
            item
            for item in sorted(base.iterdir(), key=lambda p: p.name.lower())
            if item.is_dir() and item.name not in EXCLUDED_DIRS and not item.name.startswith(".")
        ]
        if not children:
            areas.append(
                {
                    "path": rel,
                    "group": rel,
                    "file_count": count_source_files(base),
                    "samples": sample_files(root, base, limit=2),
                }
            )
            continue
        for child in children:
            areas.append(
                {
                    "path": safe_rel(root, child),
                    "group": rel,
                    "file_count": count_source_files(child),
                    "samples": sample_files(root, child, limit=2),
                }
            )
    areas = [area for area in areas if area["file_count"] > 0]
    areas.sort(key=lambda item: (-item["file_count"], item["path"]))
    return areas[:limit]


def sample_files(root: Path, base: Path, limit: int = 2) -> list[str]:
    rows: list[str] = []
    for file in sorted(base.rglob("*"), key=lambda p: safe_rel(root, p)):
        if not file.is_file():
            continue
        if file.suffix.lower() not in TEXT_SUFFIXES:
            continue
        rows.append(safe_rel(root, file))
        if len(rows) >= limit:
            break
    return rows


