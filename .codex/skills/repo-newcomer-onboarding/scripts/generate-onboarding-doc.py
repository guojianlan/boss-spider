def derive_local_url(facts: dict[str, Any]) -> str | None:
    for row in facts["commands"]:
        if row["category"] != "dev":
            continue
        match = re.search(r"--port\s+(\d+)|-p\s+(\d+)|:(\d{4,5})", row["command"])
        if not match:
            continue
        port = next((group for group in match.groups() if group), None)
        if port:
            return f"http://localhost:{port}"
    if any("Next.js" in item for item in facts["stack"]):
        return "http://localhost:3000"
    return None


def derive_core_functions(facts: dict[str, Any]) -> list[str]:
    rows: list[str] = []
