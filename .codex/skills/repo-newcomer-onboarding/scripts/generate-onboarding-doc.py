def collect_api_surface_summary(root: Path, limit: int = 8) -> list[dict[str, Any]]:
    api_root = root / "src" / "api"
    if not api_root.exists():
        return []
    rows: list[dict[str, Any]] = []
    for path in sorted(api_root.rglob("*"), key=lambda p: safe_rel(root, p)):
        if not path.is_file():
            continue
        if path.suffix.lower() not in {".ts", ".tsx", ".js", ".jsx"}:
            continue
        text = read_text(path)
        functions = EXPORTED_FUNCTION_RE.findall(text)
        if not functions:
            continue
        rows.append(
            {
                "path": safe_rel(root, path),
                "exported_functions": functions[:6],
                "export_count": len(functions),
            }
        )
    rows.sort(key=lambda item: (-item["export_count"], item["path"]))
    return rows[:limit]


