def infer_repo_shape(facts: dict[str, Any]) -> list[str]:
    inferences: list[str] = []

    entry_paths = {item["path"] for item in facts["entry_points"]}
    route_signals = facts["route_signals"]
    if "app/layout.tsx" in entry_paths and any("src/router" in item["path"] for item in facts["architecture_chain"]):
        inferences.append("推断：仓库可能采用“框架壳 + 客户端路由”或混合式入口结构。")
    if facts["generated_hints"] or facts["generated_files"]:
        inferences.append("推断：项目存在代码生成或索引生成步骤，新增页面/模块后可能需要先生成产物。")
    if not facts["test_signals"]:
        inferences.append("推断：未明显检测到自动化测试/CI 入口，质量保障可能主要依赖 lint、类型检查或人工联调。")
    if any("app/" in signal or "`app/`" in signal for signal in route_signals) and any(
        area["group"] in {"src/admin-pages", "src/pages"} for area in facts["module_areas"]
    ):
        inferences.append("推断：业务页面可能并不完全按框架默认路由目录组织，还需要结合路由映射或菜单配置理解页面落点。")

    return inferences


