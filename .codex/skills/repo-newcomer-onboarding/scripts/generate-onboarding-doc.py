def collect_env_var_keys(root: Path, limit: int = 24) -> list[str]:
    keys: set[str] = set()
    candidate_paths = [
        path for path in iter_repo_files(root)
        if path.suffix.lower() in TEXT_SUFFIXES
        and any(part in {"src", "app", "types"} for part in path.parts)
    ]
    for path in candidate_paths:
        try:
            text = read_text(path)
        except OSError:
            continue
        for match in ENV_VAR_RE.finditer(text):
            key = next((group for group in match.groups() if group), None)
            if key:
                keys.add(key)
        if len(keys) >= limit:
            break
    return sorted(keys)[:limit]


