def collect_skill_catalog(root: Path) -> list[dict[str, str]]:
    skills_dirs = [root / "skills", root / ".codex" / "skills"]
    rows: list[dict[str, str]] = []
    seen: set[str] = set()
    for skills_dir in skills_dirs:
        if not skills_dir.exists():
            continue
        for skill_md in sorted(skills_dir.glob("*/SKILL.md")):
            rel = safe_rel(root, skill_md)
            if rel in seen:
                continue
            seen.add(rel)
            text = read_text(skill_md)
            frontmatter = re.match(r"^---\n(.*?)\n---", text, re.DOTALL)
            name = skill_md.parent.name
            description = ""
            if frontmatter:
                block = frontmatter.group(1)
                name_match = re.search(r"^name:\s*(.+)$", block, re.MULTILINE)
                desc_match = re.search(r"^description:\s*(.+)$", block, re.MULTILINE)
                if name_match:
                    name = name_match.group(1).strip()
                if desc_match:
                    description = desc_match.group(1).strip()
            rows.append({"name": name, "description": description, "path": rel})
    return rows


