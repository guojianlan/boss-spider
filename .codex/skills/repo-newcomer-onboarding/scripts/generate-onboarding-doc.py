def detect_review_profiles(facts: dict[str, Any]) -> list[str]:
    profiles: list[str] = []
    stack_text = " ".join(facts.get("stack", [])).lower()

    if any(token in stack_text for token in ["next.js", "react", "typescript"]):
        profiles.append("node-frontend")
    if "react router" in stack_text:
        profiles.append("client-router")
    if "go ecosystem" in stack_text:
        profiles.append("go-service")
    if "python ecosystem" in stack_text:
        profiles.append("python-service")
    if "java/jvm ecosystem" in stack_text:
        profiles.append("java-service")
    if facts.get("generated_hints") or facts.get("generated_files"):
        profiles.append("generated-artifacts")
    if any("app/" in signal or "`app/`" in signal for signal in facts.get("route_signals", [])):
        profiles.append("framework-shell")

    seen: set[str] = set()
    ordered: list[str] = []
    for profile in profiles:
        if profile in seen:
            continue
        seen.add(profile)
        ordered.append(profile)
    return ordered


