def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="ignore")


def read_json(path: Path) -> dict[str, Any]:
    return json.loads(read_text(path))


def safe_rel(root: Path, path: Path) -> str:
    try:
        return path.relative_to(root).as_posix()
    except ValueError:
        return str(path)


def md_link(root: Path, output_path: Path, repo_path: str) -> str:
    target = root / repo_path
    rel = os.path.relpath(target, output_path.parent).replace(os.sep, "/")
    return f"[`{repo_path}`]({rel})"


def resolve_user_path(root: Path, raw_path: str) -> Path:
    candidate = Path(raw_path).expanduser()
    if candidate.is_absolute() or raw_path.startswith("~"):
        return candidate.resolve()
    return (root / raw_path).resolve()


