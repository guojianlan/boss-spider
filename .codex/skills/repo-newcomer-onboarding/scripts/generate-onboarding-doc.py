def find_command(facts: dict[str, Any], *categories: str) -> str | None:
    for category in categories:
        for row in facts["commands"]:
            if row["category"] == category:
                return row["name"]
    return None


def install_command(facts: dict[str, Any]) -> str:
    manager = facts.get("package_manager") or ""
    if "pnpm" in manager:
        return "pnpm install"
    if "yarn" in manager:
        return "yarn install"
    if "npm" in manager:
        return "npm install"
    if (Path(facts["repo_root"]) / "pyproject.toml").exists():
        return "python -m pip install -e ."
    return unresolved("未自动识别安装命令，请结合包管理器补充。")


